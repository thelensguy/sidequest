import type { ReactNode, SVGProps } from 'react';

/**
 * Shared SVG icon library, ported 1:1 (viewBox, path data, stroke
 * attributes) from the artifact-premium.html mockup's inline <svg class="i">
 * icons. These replace every emoji in src/dashboard/** and
 * src/gamification/**.
 *
 * Every icon renders at 1em (so font-size on an ancestor controls size,
 * same as the mockup's `svg.i{width:1em;height:1em}` rule) with
 * stroke="currentColor" (so `color` controls tint), 24x24 viewBox,
 * strokeWidth 1.5, no fill, round caps/joins. Callers can override any of
 * these via props (e.g. a bigger explicit width/height, or a different
 * strokeWidth) since they're spread last onto the wrapping element... note
 * they're spread FIRST here so intentional per-icon overrides (like
 * ChevronDownIcon's thicker stroke used as a select caret) still win.
 */
export type IconProps = SVGProps<SVGSVGElement>;

function Icon({ children, ...props }: IconProps & { children: ReactNode }) {
  return (
    <svg
      className="i"
      viewBox="0 0 24 24"
      width="1em"
      height="1em"
      stroke="currentColor"
      strokeWidth={1.5}
      fill="none"
      strokeLinecap="round"
      strokeLinejoin="round"
      {...props}
    >
      {children}
    </svg>
  );
}

